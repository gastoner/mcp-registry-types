import { defineConfig } from 'vite';
import { resolve } from 'path';
import dts from 'vite-plugin-dts';
import { existsSync } from 'node:fs';
import { copyFile } from 'node:fs/promises';

export default defineConfig({
  build: {
    lib: {
      entry: {
        index: resolve(__dirname, 'src/index.ts'),
        validate: resolve(__dirname, 'src/validate.ts'),
      },
      formats: ['es']
    },
    outDir: 'dist',
    sourcemap: true,
    rollupOptions: {
      external: ['ajv'],
      output: {
        preserveModules: false,
      },
    },
    emptyOutDir: true,
  },
  plugins: [
    dts({
      include: ['src/*.ts'],
      exclude: ['src/fetch-schema.ts', 'src/convert-yaml-to-json.ts'],
      afterBuild: async () => {
        if (existsSync('src/schema.d.ts')) {
          await copyFile('src/schema.d.ts', 'dist/schema.d.ts');
        }
        if (existsSync('src/openapi.d.ts')) {
          await copyFile('src/openapi.d.ts', 'dist/openapi.d.ts');
        }
      },
    }),
  ],
});

